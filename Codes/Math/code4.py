# Floor --> براکت

# [1.2] = 1
# [0.2] = 0
# [0.99999] = 0 
# [-2.99999] = -3
# [0] = 0
# [2] = 2

from math import floor

print(floor(1.2))
print(floor(0.2))
print(floor(0.99999))
print(floor(-2.99999))
print(floor(0))
print(floor(2))


print("******************")


# ceil --> سقف

from math import ceil

print(ceil(1.2))
print(ceil(0.2))
print(ceil(0.99999))
print(ceil(-2.99999))
print(ceil(0))
print(ceil(2))
